#include <iostream>

int main (){

std::cout << "Olá mundo!" << std::endl;

system("pause");

    return 0;
}

/* 

iostream: biblioteca para realizar as entradas e as saídas.
std: standard - padrão.
main: função principal.
cout: função de saída.
cin: função de entrada.
int: variável de valor inteiro.
{}: delimita o que está dentro de uma estrutura de repetição.
return 0:  retorna ao inteiro, fim da ação.
endl: quebra de linha.
\n: quebra de linha.
system("pause"): fica parado até um botão ser apertado.
using namespace std: pode usar isso no lugar de std::cout.

ex 2:

#include <iostream>

using namespace std;
int main(){

    cout << "Hello World!\n Welcome...";
    system("pause");

    return 0;
}

 */